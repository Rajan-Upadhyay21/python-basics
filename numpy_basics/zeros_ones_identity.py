import numpy as np

zeros_array = np.zeros((2, 3))
ones_array = np.ones((3, 2))
identity_matrix = np.eye(4)

print("Zeros Array:")
print(zeros_array)

print("\nOnes Array:")
print(ones_array)

print("\nIdentity Matrix:")
print(identity_matrix)
